#include <stdio.h>

void    equation(int n)
{
	int i;
	int j;
	int k;

	// iterate all numbers
	i = 0;
	while(i < 10)
	{
		j = 0;
		while (j < 10)
		{
			k = 0;
			while (k < 10)
			{
				if ((i * 10 + j) + (k * 10 + i) == n)
					printf("A = %d, B = %d, C = %d\n", i, j, k);
				k++;
			}
			j++;
		}
		i++;
	}
}
